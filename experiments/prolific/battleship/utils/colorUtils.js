export function getColor(item) {
    switch (item) {
      case -1:
        return '#DfdfDf'; //light gray
      case 0:
        return '#7abcd8'; //aqua/teal
      case 1:
        return '#da2a2a'; //red
      case 2:
        return '#3c9f5e'; //green
      case 3:
        return '#8d1fdb'; //purple
      case 4:
        return '#ed8824'; //orange
    }
  }